# Game Rules:
# You must be 18 or older (or 14 or older with a parent's permission) to play
# Level starts at 1
# Health starts at 15
# Each decision sprouts off into another decision tree
# Once health reaches 0, you've lost the game
# Once level reaches 10 or you've survived all decisions, you've won the game
# You can pick a weapon at the beginning of the game, what you pick influences the decisions you can make
# If at any point input does not match the given decisions, "invalid input" is returned
# Inside jokes and references abound!
